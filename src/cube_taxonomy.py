def classify_how2(text: str) -> list[tuple[str, str, str]]:
    """
    텍스트에서 CUBE HOW2 대공종을 매핑합니다.

    Args:
        text: proposal_title + description 결합 텍스트

    Returns:
        [(how1_code, how2_code, how2_name), ...] 매칭된 대공종 목록
    """
    results = []
    for (how1_code, how2_code), keywords in HOW2_KEYWORD_MAP.items():
        if any(kw in text for kw in keywords):
            how2_name = HOW_TAXONOMY[how1_code]["sub"][how2_code]
            results.append((how1_code, how2_code, how2_name))
    return results


def classify_space(text: str) -> list[str]:
    """
    텍스트에서 공간 유형을 추출합니다.

    Args:
        text: proposal_title + description 결합 텍스트

    Returns:
        [space_name, ...] 매칭된 공간 유형 목록
    """
    results = []
    for space_name, keywords in SPACE_KEYWORDS.items():
        if any(kw in text for kw in keywords):
            results.append(space_name)
    return results


def get_how_full_code(how1_code: str, how2_code: str) -> str:
    """CUBE HOW 전체 코드를 반환합니다. (예: A-08)"""
    return f"{how1_code}-{how2_code}"


def get_how2_name(how1_code: str, how2_code: str) -> str:
    """CUBE HOW2 대공종 이름을 반환합니다."""
    return HOW_TAXONOMY.get(how1_code, {}).get("sub", {}).get(how2_code, "기타")


